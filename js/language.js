// Function to update page language
function updatePageLanguage(lang) {
    const t = translations[lang] || translations['en'];

    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (t[key]) {
            if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
                element.placeholder = t[key];
            } else if (element.tagName === 'BUTTON' && element.id === 'main-edit-btn') {
                // Special handling for edit/save button
                const isEditing = element.textContent.toLowerCase() === 'save' || element.textContent.toLowerCase() === t.save.toLowerCase();
                element.textContent = isEditing ? t.save : t.edit;
            } else {
                element.textContent = t[key];
            }
        }
    });

    // Save language to localStorage
    localStorage.setItem('userLanguage', lang);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLanguage = localStorage.getItem('userLanguage') || 'en';
    updatePageLanguage(savedLanguage);
});
